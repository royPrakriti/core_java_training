package operator;

public class switchcase_ {
	public static void main(String[] args) {
	    
		int c = 1;
		
	    switch(c) {
	    case 1:{
	    	System.out.println("c1");
	    }
	    break;
	    case 2:{
	    	System.out.println("c2");
	    }
	    break;
	    case 3:{
	    	System.out.println("c3");
	    }
	    default:{
	    	System.out.println("no match");
	    }
	    	
	    }
	    	
	}

}
