package has_a_relationship;

public class Student {
	
	private int id;
	private String name;
	
	
	public void setid(int id) {
		this.id = id;
	}
	public void setname(String name) {
		this.name = name;
	}
	
	public int getid() {
		return id;
	}
	
	public String getname() {
		return name;
	}

}
