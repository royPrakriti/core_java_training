package Student_Management_System;

public class Person {
	
	private String name;
	private int age;
	
	
	public String getname() {
		return name;
	}
	
	public int getage() {
		return age;
	}
	
	public void setname(String name) {
		this.name = name;
	}
	
	public void setage(int age) {
		this.age = age;
	}
	

}
